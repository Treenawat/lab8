public enum Color{
	RED, GREEN, BLUE, YELLOW, CYAN, MAGENTA;
	
	private int r;
	private int g;
	private int b;
	
	private void color(int r, int g, int b){
		this.r = r;
		this.g = g;
		this.b = b;
	}
	public int getR() {
		return r;
	}
	public int getG() {
		return r;
	}
	public int getB() {
		return r;
	}
}